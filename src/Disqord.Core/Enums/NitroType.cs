namespace Disqord;

/// <summary>
///     Represents the Nitro type of a user.
/// </summary>
public enum NitroType
{
    /// <summary>
    ///     The user does not have Nitro.
    /// </summary>
    None = 0,

    /// <summary>
    ///     The user has Nitro Classic.
    /// </summary>
    Classic = 1,

    /// <summary>
    ///     The user has normal Nitro.
    /// </summary>
    Normal = 2,

    /// <summary>
    ///     The user has Nitro Basic.
    /// </summary>
    Basic = 3
}
